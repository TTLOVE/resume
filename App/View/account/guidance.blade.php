	<h3>借款问题</h3>
	<ul>
		<li>
			<h4>小诺小额信用贷为什么跳转到独立的借款页面？</h4>
			<p>小诺是和考拉合作的互联网金融平台，用户申请小诺的借贷产品，操作流程在小诺的页面进行，由小诺独立完成对用户的授信、审核、放款、回款管理。所以小诺是考拉借款流程体系外的平台，本页面以下问答适用于小诺之外的借款产品。</p>
		</li>
		<li>
			<h4>借款费用包括哪些？</h4>
			<p>常规费用为借款利息。具体费用请以借款产品申请页的费用信息为准。</p>
		</li>
		<li>
			<h4>借款前是否需要支付费用？</h4>
			<p>在您借款成功前，不会收取任何费用。</p>
		</li>
		<li>
			<h4>放款账户和到账时间</h4>
			<p>目前借款成功后直接放款到考拉商圈的账户中，一般30分钟内到账。</p>
		</li>
		<li>
			<h4>申请借款之后可以取消吗？</h4>
			<p>暂时不支持取消。</p>
		</li>
	</ul>
	<h3>还款问题</h3>
	<ul>
		<li>
			<h4> 还款方式有哪些？</h4>
			<p>考拉平台体系内支持余额自动还款、银行卡代扣还款。余额自动还款即还款日当天10:00第三方支付平台会自动从考拉商圈账户余额中划扣；银行卡代扣还款需要您在还款日前，在绑定银行卡中预留大于等于当日还款金额的资金，还款日当天第三方支付平台会自动划扣；以上两种还款方式如果余额不足，划扣不成功会提示您去主动发起还款。</p>
		</li>
		<li>
			<h4>考拉商圈账户余额能否用于还款？</h4>
			<p>考拉商圈的账户余额能够直接还款，余额自动还款即在还款日上午10:00自动从您的账户余额中划扣还款。当出现逾期，考拉商圈的账户余额可能会被冻结。</p>
		</li>
		<li>
			<h4>提前还款政策</h4>
			<p>支持提前结清欠款，具体费用请以借款产品申请页的费用信息为准。</p>
		</li>
		<li>
			<h4>如果逾期，费用如何收取，会有哪些影响？</h4>
			<p>还款日当天晚12点前仍未完成还款的账单，会被系统记录为逾期（以实际账单状态为准）。发生逾期后，会影响您的个人信用记录，同时您须支付违约金和罚息（具体费用视您申请的借款产品而定），借款额度也将被冻结。请您保持良好的还款习惯，按时还款。</p>
		</li>
		<li>
			<h4>我借款后，已经有过还款，额度为什么没恢复？</h4>
			<p>任意一笔借款，全部期次还清后，该笔借款的额度才可恢复。例如：您的总额度是5万元，已借款3万元，3个月期，待您3万3个月期的借款全部还完后，您的额度才会恢复。</p>
		</li>
	</ul>
